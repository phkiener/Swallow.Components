namespace Swallow.Components;

/// <summary>
/// A position relative to another element.
/// </summary>
public enum Position
{
    /// <summary>
    /// Above the referenced element.
    /// </summary>
    Above,

    /// <summary>
    /// Below the referenced element.
    /// </summary>
    Below,

    /// <summary>
    /// On the left side of the referenced element.
    /// </summary>
    Left,

    /// <summary>
    /// On the right side of the referenced element.
    /// </summary>
    Right
}
